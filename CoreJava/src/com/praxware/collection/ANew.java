package com.praxware.collection;

public class ANew {

		private int enno;
		private String branch;
		private double cpi;
		public int getEnno() {
			return enno;
		}
		public void setEnno(int enno) {
			this.enno = enno;
		}
		public String getBranch() {
			return branch;
		}
		public void setBranch(String branch) {
			this.branch = branch;
		}
		public double getCpi() {
			return cpi;
		}
		public void setCpi(double cpi) {
			this.cpi = cpi;
		}
		



}
